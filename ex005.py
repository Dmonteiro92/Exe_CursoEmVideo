#Leia um número e mostre seu sucessor e antecessor


print('===== Desafio 05 - Sucessor e Antecessor =====')

n1 = int(input('Digite um número inteiro:'))
n2 = n1+1
n3 = n1-1

print('Você digitou o valor: {}'.format(n1))
print('O sucessor do seu número é: {}'.format(n2))
print('O antecessor do seu número é: {}'.format(n3))